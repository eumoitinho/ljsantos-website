export const getTranslation = (language: Language, key: string): string => {
  const keys = key.split(".")
  let translation: any = translations[language]

  for (const k of keys) {
    if (translation && typeof translation === "object" && k in translation) {
      translation = translation[k]
    } else {
      // Fallback to default language if translation not found
      let fallback: any = translations["pt-BR"]
      for (const fk of keys) {
        if (fallback && typeof fallback === "object" && fk in fallback) {
          fallback = fallback[fk]
        } else {
          return key // Return the key if not found in fallback
        }
      }
      return fallback
    }
  }

  return typeof translation === "string" ? translation : key // Ensure the result is a string
}
export const getLanguageName = (language: Language): string => {
  return translations[language].common.languageName
}

export const getLanguageNative = (language: Language): string => {
  return translations[language].common.languageNative
}
